package se.opendataexchange.ethernetip4j.exceptions;

public class NotImplementedException extends Exception {

    /**
     *
     */
    private static final long serialVersionUID = -3247980874584875134L;

    /**
     *
     */
    public NotImplementedException() {
        super();
    }

    /**
     * @param arg0
     */
    public NotImplementedException(String arg0) {
        super(arg0);
    }

    /**
     * @param arg0
     * @param arg1
     */
    public NotImplementedException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    /**
     * @param arg0
     */
    public NotImplementedException(Throwable arg0) {
        super(arg0);
    }
}
